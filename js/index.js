var splash = document.querySelector('.splash');

splash.addEventListener('click', () => {
    splash.style.display = 'none';
})

splash.addEventListener('touchend', () => {
    splash.style.display = 'none';
})

splash.addEventListener('scroll', () => {
    splash.style.display = 'none';
})

splash.addEventListener('touchmove', () => {
    splash.style.display = 'none';
})

